package by.tolpawsta.text;

public class TextTypeException extends Exception {
    public TextTypeException() {
        super();
    }

    public TextTypeException(String message) {
        super(message);
    }

    public TextTypeException(String message, Throwable cause) {
        super(message, cause);
    }

    public TextTypeException(Throwable cause) {
        super(cause);
    }

}
